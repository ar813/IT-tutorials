// 1. console.log (This will show your code in console of inspect)
let arrayInFormOfConsole = [3, 5, 6, 4, 3, 5, 7]
console.log(arrayInFormOfConsole);

// 2. console.table (It is used to see result in the form of table)
let arrayInFormOfTable = [3, 5, 6, 4, 3, 5, 7]
console.table(arrayInFormOfTable);

// 3. console.error (It is used to show errors in some specific conditions)
console.error("Something went wrong")

// 4. console.warn (It is used to show warnings)
console.warn("It is just warning")

// 5. console.clear used to clear all console
console.clear()

