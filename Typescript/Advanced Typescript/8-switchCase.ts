/* ************ switch case ************ */
let grade = "F";

switch (grade) {
    case "A":
        console.log("Excelent");
        break;
    case "B":
        console.log("Good");
        break;
    case "C":
        console.log("Average");
        break;
    case "D":
        console.log("Poor");
        break;
    case "E":
        console.log("Need to work hard");
        break;
    default:
        console.log("Fail");
        break;
}