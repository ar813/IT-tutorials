# String Method

name = "Arsalan Khan"

print(name.title())
print(name.lower())
print(name.upper())
print(name.capitalize())

# Concatenation

first_name = "Arsalan"
last_name = "Khan"
full_name = first_name + " " + last_name
print("Full Name:", full_name)

# OR

full_name1 = f"{first_name} {last_name}"
print("Full Name:", full_name1)

# Tab and New Line

print("\tIt is commonly used in text formatting to indicate a page \nbreak in old printers and text files.")